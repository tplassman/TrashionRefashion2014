    <p>The annual Trashion Refashion Show is a community fashion event that challenges designers of all levels to change the landscape of the fashion industry. With the increase of low garment price tags fueled by a fast fashion industry, consumers are encouraged to buy new and buy a lot. Promoting sustainable design practices, the annual runway show illuminates the importance of recognizing both environmental and social consequences of pollution and waste generation associated with the fashion industry. The Trashion Refashion Show serves as a fundraiser for the Center for Sustainable Living and showcases designs from upcycled clothing and discarded materials. Live music, aerial performers, the Hudsucker Posse, the Trashion-Off competition, and upcycled clothing and accessories for sale from Discardia round out the 5th year of this event. From the beginning to end, the message of the show is clear: We can change our relationship with clothes, one garment at a time. </p>
